var addMarker = function(markerSpec) {
  return new google.maps.Marker({
    position: markerSpec.location,
    title: markerSpec.title,
    map: map,
    icon: {
      path: google.maps.SymbolPath.CIRCLE,
      scale: 3,
      strokeColor: 'blue'
    }
  });
};

